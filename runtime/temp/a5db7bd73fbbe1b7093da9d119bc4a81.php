                            <h2 style="text-align: center;"><span style="font-family: arial, helvetica, sans-serif;">来 来 来 都坐下 我们来聊聊</span></h2>
                            <p>哎，那谁，独秀同志，你坐下，下面是我发言，还没轮到你“发炎”。</p>
                            <p>emm,我，你们可以叫我小霖，桂中山区里的一个山里娃，做为一个山里娃，下河摸鱼，上树掏鸟蛋，那叫一个6啊。</p>
                            <blockquote>
                                <p>长相一般，身高不高，你要是纠结想知道我长什么样，emm，大概就这个样吧 &nbsp;
                                    <img style="margin-bottom: -15px;" title="1532186262587134.png" src="/blog/public//static/index/picture/1532186262587134.png" alt="1532186262587134.png" width="56" height="60">
                                </p>
                            </blockquote>
                            <p>渐渐长大了，对外面的世界充满了好奇，河里的鱼越来越少了，也不知道山外面的世界是什么样的，听说山外面大河里的鱼很多很大。
                                <br />就这样，怀揣着梦想，我走出了大山，来到了大城市，在一个不怎么样的大学，开始了我的大学生涯。
                                <br />啧啧啧，不得不说，大城市的网速就是快。
                                <br />计算机网络技术专业的我，精通C、CSS、C++、C#、Java、JavaScript、ActionScript、Python、PHP、HTML等单词的拼写
                                <br />擅长AU、AE、AI、PS、PR、AutoCAD、Office等软件的安装与卸载,熟悉Windows、Linux、MAC、Android、IOS等操作系统的开关机。</p>
                            <p>
                                <img src="/blog/public//static/index/picture/null32fb351af035ee53.jpg" alt="" width="446" height="414" class="aligncenter size-full wp-image-137">
                            </p>
                            <blockquote>
                                <p>原本不学无术的我，大二开始渐渐对网络和前端产生了兴趣，先后参加了2017年区计算机网络应用比赛与第十一届广西高校计算机应用大赛（网页设计），可惜都是老二。
                                    <br />凭借对网络的了解，与对云计算和服务器的研究，实习选择成为一个云计算工程师，在HCXX Cloud Storage中挥汗如雨，不是在机房就是在去机房的路上。
                                    <br />
                                    <img src="/blog/public//static/index/picture/20180722213835.jpg" alt="" width="256" height="255" class="aligncenter size-full wp-image-138">
                                    <br />觉得太没意思了，果断跳坑，跳进了前端这个大坑，自此成为了一个苦逼的程序猿。</p>
                            </blockquote>
                            <p><strong>大学毕业，正式加入前端这个大家庭，身为一个小萌新，以为程序猿是这样。</strong>
                            </p>
                            <p>
                                <img src="/blog/public//static/index/picture/null759db2bda4a784d5.jpg" alt="" width="500" height="412" class="aligncenter size-full wp-image-139">
                            </p>
                            <p><strong>结果却是这样的</strong>
                            </p>
                            <p>
                                <img src="/blog/public//static/index/picture/null-37afdd003647d38d.jpg" alt="" width="410" height="242" class="aligncenter size-full wp-image-140">
                            </p>
                            <p>
                                <img src="/blog/public//static/index/picture/20180722210118.jpg" alt="" width="450" height="380" class="aligncenter size-full wp-image-141">
                            </p>
                            <blockquote>
                                <p>千里之行始于足下，不积跬步无以至千里，每天一个小脚印，慢慢向前前进着，大佬们，等等我。</p>
                            </blockquote>
